function getRandomInt(max) {
  return Math.floor(Math.random() * Math.floor(max));
}

interface options {
  minLength?: number;
  maxLength?: number;
}
export const getWord = ({ minLength: minSize = 0, maxLength: maxSize = 9999 }: options): string => {
  const potentialWords = words.filter(word => word.length >= minSize && word.length <= maxSize);
  const wordIndex = getRandomInt(potentialWords.length - 1);
  return potentialWords[wordIndex];
};
